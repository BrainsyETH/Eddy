function colorForPercentile(p: number | null | undefined): string {
  if (p == null || isNaN(p)) return '#857D70';
  return classifyPercentile(p).color;
}

function strokeWidthForRiver(
  lengthMiles: number | null | undefined,
  hovered: boolean,
  focused: boolean,
): number {
  // Curated rivers sit on top of the basemap network; keep them visible but
  // not so heavy they read as the only rivers in the state.
  const m = lengthMiles ?? 60;
  const base = Math.max(2.6, Math.min(4.8, 2.2 + m / 60));
  if (focused) return base + 2.4;
  if (hovered) return base + 1.4;
  return base;
}

function strokeWidthForBasemapRiver(lengthMi: number): number {
  // Length-weighted strokes for the NHD basemap network. The previous
  // ramp made small named tributaries (e.g. Jacks Fork at 46 mi) render
  // at 0.7 viewBox-units, which is sub-pixel at full state framing and
  // visually disappears. Bumped across the board so the basemap reads
  // as a real river map at all zoom levels.
  if (lengthMi >= 200) return 2.4;
  if (lengthMi >= 100) return 1.8;
  if (lengthMi >= 50) return 1.4;
  return 1.0;
}

export default function MOMap(props: MOMapProps) {
  // Lock the projection window to the full Missouri state outline so the
  // map always reads as "Missouri", no matter what data subset is loaded.
  // Earlier we framed to the data bbox, which caused the southern half of
  // the state to fill the canvas and the visual to drift as data changed.
  const bbox = useMemo(() => {
    let minLon = Infinity, maxLon = -Infinity;
    let minLat = Infinity, maxLat = -Infinity;
    for (const [lon, lat] of MO_OUTLINE) {
      if (lon < minLon) minLon = lon;
      if (lon > maxLon) maxLon = lon;
      if (lat < minLat) minLat = lat;
      if (lat > maxLat) maxLat = lat;
    }
    // Tight padding — the state outline already has visual breathing room.
    const lonPad = (maxLon - minLon) * 0.02;
    const latPad = (maxLat - minLat) * 0.02;
    return {
      minLon: minLon - lonPad,
      maxLon: maxLon + lonPad,
      minLat: minLat - latPad,
      maxLat: maxLat + latPad,
    };
  }, []);

  const project = useMemo(() => buildProjector(bbox), [bbox]);

  const stateOutlinePath = useMemo(
    () => polygonToPath(MO_OUTLINE, project),
    [project],
  );
  const riverPaths = useMemo(() => {
    const out: Record<string, string> = {};
    for (const r of props.rivers) {
      out[r.id] = lineToPath(r.geometry.coordinates as Array<[number, number]>, project);
    }
    return out;
  }, [props.rivers, project]);

  // Per-river gradient stops along each curated river. Replaces the
  // hard-edged per-segment palette with a single SVG <linearGradient>
  // whose stops sit at each gauge's projected position along the line —
  // so adjacent gauges' colors fade smoothly into each other instead of
  // flipping at midpoints.
  //
  // Output: { [riverId]: { x1,y1,x2,y2, stops: Array<{ offset, color, condition, siteId }> } }
  // The gradient direction is the straight line from the river's first
  // projected vertex to its last. For mostly-N-S or mostly-E-W rivers
  // (which is most of MO) this tracks the flow direction. Hard-curving
  // rivers will paint with some color drift relative to local flow, but
  // the smooth fade reads cleanly regardless.
  const riverGradients = useMemo(() => {
    const out: Record<
      string,
      {
        x1: number; y1: number; x2: number; y2: number;
        stops: Array<{ offset: number; color: string; condition: StageVerdict; siteId: string }>;
      }
    > = {};
    for (const r of props.rivers) {
      const coords = r.geometry.coordinates as Array<[number, number]>;
      if (coords.length < 2) continue;
      const first = project(coords[0][0], coords[0][1]);
      const last = project(coords[coords.length - 1][0], coords[coords.length - 1][1]);
      const dx = last[0] - first[0];
      const dy = last[1] - first[1];
      const lenSq = dx * dx + dy * dy;
      if (lenSq < 1) continue; // degenerate

      const gs = (r.gauges ?? [])
        .filter((g) => props.conditionByGauge[g.site_id])
        .map((g) => {
          const [gx, gy] = project(g.lon, g.lat);
          // Project (gx,gy) onto the (first,last) gradient line; t in [0,1].
          const t = ((gx - first[0]) * dx + (gy - first[1]) * dy) / lenSq;
          return {
            siteId: g.site_id,
            condition: props.conditionByGauge[g.site_id],
            offset: Math.max(0, Math.min(1, t)),
          };
        })
        .sort((a, b) => a.offset - b.offset);
      if (!gs.length) continue;

      // Build stops. SVG interpolates linearly between stops, so
      // adjacent gauges fade smoothly. We extend the first/last stops
      // to offsets 0 and 1 so the river ends paint in their nearest
      // gauge's color rather than gradient-default.
      const stops: Array<{ offset: number; color: string; condition: StageVerdict; siteId: string }> = [];
      const firstColor = STAGE_VERDICTS[gs[0].condition].color;
      const lastColor = STAGE_VERDICTS[gs[gs.length - 1].condition].color;
      if (gs[0].offset > 0.001) {
        stops.push({ offset: 0, color: firstColor, condition: gs[0].condition, siteId: gs[0].siteId });
      }
      for (const g of gs) {
        stops.push({ offset: g.offset, color: STAGE_VERDICTS[g.condition].color, condition: g.condition, siteId: g.siteId });
      }
      if (gs[gs.length - 1].offset < 0.999) {
        stops.push({ offset: 1, color: lastColor, condition: gs[gs.length - 1].condition, siteId: gs[gs.length - 1].siteId });
      }

      out[r.id] = { x1: first[0], y1: first[1], x2: last[0], y2: last[1], stops };
    }
    return out;
  }, [props.rivers, props.conditionByGauge, project]);

  // Basemap rivers — pre-projected once per `project` change, sorted by
  // length so longer rivers paint underneath shorter ones (so the smaller
  // tributaries don't get covered up).
  const basemapRiverPaths = useMemo(() => {
    return BASEMAP_RIVERS
      .map((f) => ({
        name: f.properties.name,
        lengthMi: f.properties.length_mi,
        d: lineToPath(f.geometry.coordinates as Array<[number, number]>, project),
      }))
      .filter((r) => r.d.length > 0)
      .sort((a, b) => b.lengthMi - a.lengthMi);
  }, [project]);

  // Basemap lakes — flatten Polygon and MultiPolygon outer rings into
  // individual SVG path strings. Holes (inner rings) are ignored at this
  // simplification level; reservoir holes don't render meaningfully here.
  const basemapLakePaths = useMemo(() => {
    const out: Array<{ name: string; d: string }> = [];
    for (const f of BASEMAP_LAKES) {
      const rings: Array<Array<[number, number]>> = [];
      if (f.geometry.type === 'Polygon') {
        const outer = (f.geometry.coordinates as number[][][])[0];
        if (outer) rings.push(outer as Array<[number, number]>);
      } else {
        for (const poly of f.geometry.coordinates as number[][][][]) {
          if (poly[0]) rings.push(poly[0] as Array<[number, number]>);
        }
      }
      const d = rings.map((r) => polygonToPath(r, project)).join(' ');
      if (d) out.push({ name: f.properties.name, d });
    }
    return out;
  }, [project]);

  // Cities visible in the framed area (plus a small buffer outside, so the
  // nearest metro still anchors the user's orientation).
  const visibleCities = useMemo(() => {
    const lonBuf = (bbox.maxLon - bbox.minLon) * 0.18;
    const latBuf = (bbox.maxLat - bbox.minLat) * 0.18;
    return CITIES.filter(
      (c) =>
        c.lon >= bbox.minLon - lonBuf &&
        c.lon <= bbox.maxLon + lonBuf &&
        c.lat >= bbox.minLat - latBuf &&
        c.lat <= bbox.maxLat + latBuf,
    );
  }, [bbox]);

  // ─── Flow animation ────────────────────────────────────────────────────
  // The river segments paint as flowing dashed lines via CSS-animated
  // stroke-dashoffset. Each ConditionCode maps to a loop duration: too_low
  // is ~60s per dash period (almost stationary), dangerous is ~2s
  // (sprinting). Replaces the old RAF-driven particle dots — same visual
  // intent (motion communicates flow strength) but seamless and free, no
  // JS frame loop, and crucially no "no particles for too_low/low" gap
  // since every segment animates regardless of condition.
  //
  // The dash period (sum of dash + gap, in viewBox units) needs to match
  // the negative `to` value on the @keyframes below — they're both 22.
  const FLOW_DURATION_BY_CONDITION: Record<StageVerdict, number> = {
    too_low:   60,
    low:       30,
    good:      14,
    flowing:   8,
    high:      4,
    dangerous: 2,
    unknown:   20,
  };

  // ─── Zoom + pan ────────────────────────────────────────────────────────
  // The SVG viewBox is state. Wheel zooms around the cursor; primary-button
  // drag pans; double-click resets to the full-state framing. Children
  // never re-project (paths are memoized on `project`); only the viewBox
  // attribute changes per frame, so the browser handles zoom natively.
  const HOME_VIEW = useMemo(() => ({ x: 0, y: 0, w: W, h: H }), []);
  const [view, setView] = useState(HOME_VIEW);
  const dragRef = useRef<
    | {
        pointerId: number;
        startClientX: number;
        startClientY: number;
        startViewX: number;
        startViewY: number;
        moved: boolean;
        captured: boolean;
      }
    | null
  >(null);

  const clampView = useCallback(
    (v: { x: number; y: number; w: number; h: number }) => {
      // Cap zoom: 12x in (so individual rivers are readable) and 1.4x out
      // (so the user can't drift into open ocean).
      const minW = W / 12;
      const maxW = W * 1.4;
      const nw = Math.max(minW, Math.min(maxW, v.w));
      const nh = nw * (H / W);
      // Pan limits: keep the center inside [-W/4, 5W/4] x [-H/4, 5H/4]
      const cx = v.x + v.w / 2;
      const cy = v.y + v.h / 2;
      const ccx = Math.max(-W / 4, Math.min((5 * W) / 4, cx));
      const ccy = Math.max(-H / 4, Math.min((5 * H) / 4, cy));
      return { x: ccx - nw / 2, y: ccy - nh / 2, w: nw, h: nh };
    },
    [],
  );

  const onWheel = useCallback(
    (e: React.WheelEvent<SVGSVGElement>) => {
      e.preventDefault();
      const svg = e.currentTarget;
      const rect = svg.getBoundingClientRect();
      // Cursor in viewBox coords (the world point under the pointer).
      const px = view.x + ((e.clientX - rect.left) / rect.width) * view.w;
      const py = view.y + ((e.clientY - rect.top) / rect.height) * view.h;
      // Trackpads emit small deltas; mouse wheels emit ~100. Normalize so
      // both feel similar.
      const dy = e.deltaMode === 1 ? e.deltaY * 16 : e.deltaY;
      const factor = Math.exp(dy / 600);
      const nw = view.w * factor;
      const nh = nw * (H / W);
      // Re-anchor so the world point under the cursor stays put.
      const nx = px - ((e.clientX - rect.left) / rect.width) * nw;
      const ny = py - ((e.clientY - rect.top) / rect.height) * nh;
      setView(clampView({ x: nx, y: ny, w: nw, h: nh }));
    },
    [view, clampView],
  );

  // Drag threshold (pixels of pointer movement before we treat the gesture
  // as a pan). Below this we leave the click path untouched so feature
  // markers receive their click events normally.
  const DRAG_THRESHOLD_PX = 5;

  const onPointerDown = useCallback((e: React.PointerEvent<SVGSVGElement>) => {
    if (e.button !== 0) return;
    // Track potential drag, but DO NOT capture the pointer yet. If we
    // capture here, all subsequent pointer events (including the click
    // that follows pointerup) get retargeted to the SVG, and clicks on
    // markers, the zoom buttons, etc. never reach their original target
    // — diagnosed via /tmp/click-debug.mjs which showed the click event
    // landing on `<svg>` with no marker target. We capture lazily in
    // onPointerMove, only once the user has actually moved past the
    // drag threshold.
    dragRef.current = {
      pointerId: e.pointerId,
      startClientX: e.clientX,
      startClientY: e.clientY,
      startViewX: view.x,
      startViewY: view.y,
      moved: false,
      captured: false,
    };
  }, [view.x, view.y]);

  const onPointerMove = useCallback((e: React.PointerEvent<SVGSVGElement>) => {
    const drag = dragRef.current;
    if (!drag) return;
    const dx = e.clientX - drag.startClientX;
    const dy = e.clientY - drag.startClientY;
    if (!drag.moved &&
        (Math.abs(dx) > DRAG_THRESHOLD_PX || Math.abs(dy) > DRAG_THRESHOLD_PX)) {
      drag.moved = true;
      // Once we know this is a real drag, capture the pointer so events
      // keep flowing even if the cursor leaves the SVG bounds mid-pan.
      try {
        e.currentTarget.setPointerCapture(drag.pointerId);
        drag.captured = true;
      } catch { /* nothing useful we can do */ }
    }
    if (!drag.moved) return;
    const rect = e.currentTarget.getBoundingClientRect();
    const sx = view.w / rect.width;
    const sy = view.h / rect.height;
    setView((v) => clampView({
      ...v,
      x: drag.startViewX - dx * sx,
      y: drag.startViewY - dy * sy,
    }));
  }, [view.w, view.h, clampView]);

  const onPointerUp = useCallback((e: React.PointerEvent<SVGSVGElement>) => {
    const drag = dragRef.current;
    if (drag?.captured) {
      try { e.currentTarget.releasePointerCapture(drag.pointerId); } catch { /* ignore */ }
    }
    // Don't null `dragRef` synchronously — onClick reads `moved` to know
    // whether this was a real click or the tail of a drag. Cleared next
    // tick.
    setTimeout(() => { dragRef.current = null; }, 0);
  }, []);

  const resetView = useCallback(() => setView(HOME_VIEW), [HOME_VIEW]);

  // Scale factor for "non-scaling" SVG content. Multiply any radius or
  // stroke-width that should stay screen-pixel-stable as the user zooms
  // in by `kStable`. Without this, a 4px gauge dot at 1× zoom becomes a
  // 24px blob at 6× zoom because viewBox-units shrink relative to screen
  // pixels.
  const kStable = view.w / W;

  const zoomBy = useCallback((factor: number) => {
    setView((v) => clampView({
      x: v.x + (v.w * (1 - factor)) / 2,
      y: v.y + (v.h * (1 - factor)) / 2,
      w: v.w * factor,
      h: v.h * factor,
    }));
  }, [clampView]);
  const zoomedIn = view.w < W * 0.99;

  // Click handlers on individual features need to ignore the tail of a
  // drag-pan; otherwise dragging across a river or gauge focuses it on
  // mouseup. Wrap each feature click with this guard.
  const guardClick = useCallback(
    <E extends { stopPropagation: () => void }>(fn: (e: E) => void) => (e: E) => {
      if (dragRef.current?.moved) return;
      e.stopPropagation();
      fn(e);
    },
    [],
  );

  // Sort rivers so lower-order paint underneath higher-order, and the
  // hovered/focused one always renders last (on top).
  const orderedRivers = useMemo(() => {
    return [...props.rivers].sort((a, b) => {
      const aTop = a.id === props.hoveredRiverId || a.id === props.focusedRiverId;
      const bTop = b.id === props.hoveredRiverId || b.id === props.focusedRiverId;
      if (aTop && !bTop) return 1;
      if (bTop && !aTop) return -1;
      return (a.length_miles ?? 0) - (b.length_miles ?? 0);
    });
  }, [props.rivers, props.hoveredRiverId, props.focusedRiverId]);

  return (
    <>
    <svg
      viewBox={`${view.x} ${view.y} ${view.w} ${view.h}`}
      preserveAspectRatio="xMidYMid meet"
      className="absolute inset-0 h-full w-full"
      style={{ cursor: dragRef.current?.moved ? 'grabbing' : 'grab', touchAction: 'none' }}
      onWheel={onWheel}
      onPointerDown={onPointerDown}
      onPointerMove={onPointerMove}
      onPointerUp={onPointerUp}
      onPointerCancel={onPointerUp}
      onDoubleClick={resetView}
      onClick={(e) => {
        // Empty-area click clears focus — but only if we didn't just pan.
        if (dragRef.current?.moved) return;
        if (e.target === e.currentTarget) {
          props.onFocusRiver(null);
          props.onFocusGauge(null);
          props.onClickCampground(null);
          props.onClickAccessPoint(null);
          props.onClickPoi(null);
        }
      }}
    >
      <defs>
        <radialGradient id="mo-bg" cx="50%" cy="50%" r="85%">
          <stop offset="0%" stopColor="#F4ECDB" />
          <stop offset="60%" stopColor="#EADFC2" />
          <stop offset="100%" stopColor="#D4C394" />
        </radialGradient>
        <pattern id="mo-grain" width="180" height="180" patternUnits="userSpaceOnUse">
          {Array.from({ length: 70 }).map((_, i) => {
            const seed = (i * 9301 + 49297) % 233280;
            const x = seed % 180;
            const y = ((seed / 180) | 0) % 180;
            const r = ((seed % 7) / 10) + 0.3;
            return <circle key={i} cx={x} cy={y} r={r} fill="rgba(105,80,40,0.07)" />;
          })}
        </pattern>
        {/* Clip path so the parchment fill respects MO's borders even when
            the silhouette overflows the viewBox. */}
        <clipPath id="mo-clip">
          <path d={stateOutlinePath} />
        </clipPath>
      </defs>

      {/* Backdrop (matches Eddy primary-900) */}
      <rect width={W} height={H} fill="#0F2D35" />

      {/* State silhouette — parchment fill, clipped to state, plus a stroked
          border for definition. The state path may extend off-canvas; SVG
          clips it naturally. */}
      <g clipPath="url(#mo-clip)">
        <rect width={W} height={H} fill="url(#mo-bg)" />
        <rect width={W} height={H} fill="url(#mo-grain)" />

        {/* NHD basemap — lakes (filled polygons) under named perennial
            rivers (thin uniform blue). Renders inside the state clip so it
            never bleeds across MO's borders. Curated rivers, gauges, etc.
            paint OUTSIDE this group on top of the basemap. */}
        <g pointerEvents="none">
          {basemapLakePaths.map((l) => (
            <path
              key={`lake-${l.name}`}
              d={l.d}
              fill="#7FB2C2"
              stroke="#3F8499"
              strokeWidth={0.6}
              opacity={0.78}
            />
          ))}
          {basemapRiverPaths.map((r) => (
            <path
              key={`bm-${r.name}`}
              d={r.d}
              stroke="#3F8499"
              strokeWidth={strokeWidthForBasemapRiver(r.lengthMi)}
              fill="none"
              strokeLinecap="round"
              strokeLinejoin="round"
              opacity={0.85}
            />
          ))}
        </g>
      </g>
      <path
        d={stateOutlinePath}
        fill="none"
        stroke="rgba(80,60,30,0.65)"
        strokeWidth={2.2}
        strokeLinejoin="round"
      />

      {/* Title */}
      <text
        x={W / 2}
        y={48}
        textAnchor="middle"
        fontFamily="var(--font-mono), ui-monospace, monospace"
        fontSize={13}
        letterSpacing="0.45em"
        fill="rgba(80,60,30,0.5)"
        fontWeight={500}
      >
        STATE OF MISSOURI · SURFACE WATER NETWORK
      </text>

      {/* Cities */}
      <g>
        {visibleCities.map((c) => {
          const [cx, cy] = project(c.lon, c.lat);
          const cityR = (c.type === 'metro' ? 4 : 2.8) * kStable;
          const labelOff = (c.type === 'metro' ? 8 : 6) * kStable;
          const labelSize = (c.type === 'metro' ? 13 : 11) * kStable;
          return (
            <g key={c.name} transform={`translate(${cx} ${cy})`}>
              <circle
                r={cityR}
                fill="rgba(45,42,36,0.78)"
                stroke="rgba(247,246,243,0.9)"
                strokeWidth={1.2 * kStable}
              />
              <text
                x={labelOff}
                y={4 * kStable}
                fontFamily="var(--font-mono), ui-monospace, monospace"
                fontSize={labelSize}
                fontWeight={c.type === 'metro' ? 700 : 500}
                fill="rgba(45,42,36,0.85)"
                paintOrder="stroke"
                stroke="rgba(242,234,216,0.92)"
                strokeWidth={2.5 * kStable}
                strokeLinejoin="round"
              >
                {c.name}
              </text>
            </g>
          );
        })}
      </g>

      {/* Rivers — outer "casing" + per-gauge segmented main stroke. Each
          river is split at midpoints between its gauges and painted in
          bands so the river color "flows into" each gauge instead of
          showing one mean color across the whole reach.

          Wrapped in `clipPath="url(#mo-clip)"` so the curated lines stay
          inside MO's borders — Eleven Point's lower NHD reach legitimately
          continues into Arkansas, but on a Missouri-focused map we clip
          it the same way the basemap is clipped. */}
      <g clipPath="url(#mo-clip)">
      {orderedRivers.map((r) => {
        const d = riverPaths[r.id];
        if (!d) return null;
        const slug = r.slug;
        const verdict = props.verdictByRiver[slug] ?? 'unknown';
        const fallbackColor = STAGE_VERDICTS[verdict].color;
        const isHovered = props.hoveredRiverId === r.id;
        const isFocused = props.focusedRiverId === r.id;
        const sw = strokeWidthForRiver(r.length_miles, isHovered, isFocused);
        const dim =
          (props.focusedRiverId && !isFocused) ||
          (props.hoveredRiverId && !isHovered && !isFocused);
        const grad = riverGradients[r.id];
        const gradId = `mo-river-grad-${r.id}`;
        const gradStrokeRef = grad ? `url(#${gradId})` : fallbackColor;
        // Flow speed for the whole river is driven by the primary gauge's
        // condition (or the river's overall verdict if there's no
        // primary). Each river paints at one speed; color fades along
        // the gradient at each gauge's projected position.
        const flowDur = FLOW_DURATION_BY_CONDITION[verdict];
        // Highlight the dangerous portion with a brief dashed inner
        // overlay if any gauge is at flood — limited to the segment
        // around that gauge so neighbors aren't visually contaminated.
        const floodGauges = grad?.stops.filter((s) => s.condition === 'dangerous') ?? [];

        return (
          <g
            key={r.id}
            style={{ opacity: dim ? 0.32 : 1, transition: 'opacity 200ms', cursor: 'pointer' }}
            onMouseEnter={() => props.onHoverRiver(r.id)}
            onMouseLeave={() => props.onHoverRiver(null)}
            onClick={guardClick(() => props.onFocusRiver(r.id))}
          >
            {grad && (
              <defs>
                <linearGradient
                  id={gradId}
                  gradientUnits="userSpaceOnUse"
                  x1={grad.x1}
                  y1={grad.y1}
                  x2={grad.x2}
                  y2={grad.y2}
                >
                  {grad.stops.map((s, i) => (
                    <stop key={`${gradId}-stop-${i}`} offset={s.offset} stopColor={s.color} />
                  ))}
                </linearGradient>
              </defs>
            )}
            {/* Hit target */}
            <path d={d} stroke="transparent" strokeWidth={Math.max(18, sw * 3)} fill="none" strokeLinecap="round" />
            {/* Glow on hover/focus */}
            {(isHovered || isFocused) && (
              <path
                d={d}
                stroke={fallbackColor}
                strokeWidth={sw + 7}
                fill="none"
                strokeLinecap="round"
                opacity={0.35}
                style={{ filter: `drop-shadow(0 0 6px ${fallbackColor})` }}
              />
            )}
            {/* Solid gradient base — colors fade smoothly between adjacent
                gauges via the linearGradient stops above. One path per
                river instead of N segments. */}
            <path
              d={d}
              stroke={gradStrokeRef}
              strokeWidth={sw}
              fill="none"
              strokeLinecap="round"
              strokeLinejoin="round"
            />
            {/* Animated flow overlay — same gradient, dashed, animated
                stroke-dashoffset. Speed comes from the river's overall
                verdict (driven by the primary gauge). */}
            <path
              d={d}
              stroke={gradStrokeRef}
              strokeWidth={Math.max(1.1, sw * 0.55)}
              fill="none"
              strokeLinecap="round"
              strokeDasharray="14 8"
              opacity={0.85}
              style={{
                animation: `mo-river-flow ${flowDur}s linear infinite`,
                mixBlendMode: 'screen',
              }}
            />
            {/* Flood emphasis — when ANY gauge is at flood, draw a brief
                dashed overlay so the hazard reads even through the fade. */}
            {floodGauges.length > 0 && (
              <path
                d={d}
                stroke={STAGE_VERDICTS.dangerous.inner}
                strokeWidth={Math.max(1.2, sw * 0.42)}
                fill="none"
                strokeLinecap="round"
                strokeDasharray="4 3"
                opacity={0.7}
              />
            )}
          </g>
        );
      })}
      </g>

      {/* Flow animation keyframes — applied per-segment via inline style.
          One dash period = 22 viewBox units (14 dash + 8 gap). */}
      <style>{`
        @keyframes mo-river-flow { to { stroke-dashoffset: -22; } }
      `}</style>

      {/* Gauges. Radii multiplied by kStable so the dot stays the same
          *screen* size at any zoom — at the state-wide view it doesn't
          dwarf the rivers, and zoomed in it doesn't balloon.
          Each marker has an invisible 14px screen hit-target as the
          first child so clicks register reliably even when the visible
          dot is only a few pixels across. */}
      {props.showGauges && (
      <g>
        {props.gauges.map((g) => {
          const r = props.rivers.find((x) => x.id === g.river_id);
          const gauge = r?.gauges?.find((x) => x.site_id === g.site_no);
          if (!gauge) return null;
          const [x, y] = project(gauge.lon, gauge.lat);
          const p = props.percentileByGauge[g.site_no] ?? g.percentile;
          const color = colorForPercentile(p);
          const isPeak = p != null && p >= 75;
          const isHovered = props.hoveredGaugeId === g.site_no;
          const isFocused = props.focusedGaugeId === g.site_no;
          const verdict = props.conditionByGauge[g.site_no] ?? 'unknown';
          const iconHref = getEddyImageForCondition(verdict);
          // Marker size in SVG units; primary gauges + hover/focus scale up.
          const baseSize = isFocused ? 32 : isHovered ? 28 : g.is_primary ? 22 : 16;
          const size = baseSize * kStable;
          return (
            <g
              key={g.site_no}
              transform={`translate(${x} ${y})`}
              style={{ cursor: 'pointer' }}
              onMouseEnter={() => props.onHoverGauge(g.site_no)}
              onMouseLeave={() => props.onHoverGauge(null)}
              onClick={guardClick(() => props.onFocusGauge(g.site_no))}
            >
              <circle r={Math.max(14, baseSize * 0.6) * kStable} fill="transparent" pointerEvents="all" />
              {isPeak && (
                <circle r={(baseSize * 0.55) * kStable} fill="none" stroke={color} strokeWidth={1.2 * kStable} opacity={0.7} pointerEvents="none">
                  <animate attributeName="r" from={(baseSize * 0.55) * kStable} to={(baseSize * 1.1) * kStable} dur="2.2s" repeatCount="indefinite" />
                  <animate attributeName="opacity" from="0.7" to="0" dur="2.2s" repeatCount="indefinite" />
                </circle>
              )}
              <image
                href={iconHref}
                x={-size / 2}
                y={-size / 2}
                width={size}
                height={size}
                pointerEvents="none"
                style={{
                  filter: isFocused
                    ? 'drop-shadow(0 1px 2px rgba(15,45,53,0.55))'
                    : 'drop-shadow(0 1px 1.5px rgba(15,45,53,0.35))',
                }}
              />
            </g>
          );
        })}
      </g>
      )}

      {/* Access points */}
      {props.showAccessPoints && (
        <g>
          {props.rivers.flatMap((r) =>
            (r.access_points ?? []).map((a) => {
              const [x, y] = project(a.lon, a.lat);
              return (
                <g
                  key={a.id}
                  transform={`translate(${x} ${y})`}
                  style={{ cursor: 'pointer' }}
                  onClick={guardClick(() => props.onClickAccessPoint(a.id))}
                >
                  <circle r={14 * kStable} fill="transparent" pointerEvents="all" />
                  <circle r={3.6 * kStable} fill="#4EB86B" stroke="#1A3D23" strokeWidth={1.1 * kStable} pointerEvents="none" />
                </g>
              );
            }),
          )}
        </g>
      )}

      {/* Campgrounds */}
      {props.showCampgrounds && (
        <g>
          {props.campgrounds.map((c) => {
            const [x, y] = project(c.lon, c.lat);
            const screenR = c.total_sites != null
              ? Math.min(7, 3.5 + c.total_sites / 50)
              : 3.5;
            return (
              <g
                key={c.id}
                transform={`translate(${x} ${y})`}
                style={{ cursor: 'pointer' }}
                onClick={guardClick(() => props.onClickCampground(c.id))}
              >
                <circle r={Math.max(14, screenR + 8) * kStable} fill="transparent" pointerEvents="all" />
                <circle r={screenR * kStable} fill="#7A684B" stroke="#F2EAD8" strokeWidth={1.4 * kStable} opacity={0.95} pointerEvents="none" />
              </g>
            );
          })}
        </g>
      )}

      {/* POIs */}
      {props.showPOIs && (
        <g>
          {props.rivers.flatMap((r) =>
            (r.pois ?? []).map((p) => {
              const [x, y] = project(p.lon, p.lat);
              const tone = POI_TONES[p.type] ?? '#524D43';
              return (
                <g
                  key={p.id}
                  transform={`translate(${x} ${y})`}
                  style={{ cursor: 'pointer' }}
                  onClick={guardClick(() => props.onClickPoi(p.id))}
                >
                  <circle r={14 * kStable} fill="transparent" pointerEvents="all" />
                  <circle r={4.5 * kStable} fill={tone} stroke="#FAF8F4" strokeWidth={1.4 * kStable} pointerEvents="none" />
                </g>
              );
            }),
          )}
        </g>
      )}

      {/* Period of record footer note */}
      <text
        x={W - 14}
        y={H - 14}
        textAnchor="end"
        fontFamily="var(--font-mono), ui-monospace, monospace"
        fontSize={10}
        fill="rgba(80,60,30,0.55)"
      >
        Geometry: USGS NHD via Supabase · Live data: USGS NWIS
      </text>

      {/* Reference key for percentile coloring */}
      <text
        x={14}
        y={H - 14}
        fontFamily="var(--font-mono), ui-monospace, monospace"
        fontSize={9}
        letterSpacing="0.1em"
        fill="rgba(80,60,30,0.55)"
      >
        Stream colour: percentile vs. period of record · {PERCENTILE_CLASSES.length} bands
      </text>
    </svg>

    {/* Zoom controls — overlay, not inside the SVG, so they stay fixed
        in screen coordinates regardless of the current view. */}
    <div
      className="absolute right-4 top-1/2 -translate-y-1/2 flex flex-col gap-1 z-10"
      style={{ fontFamily: 'var(--font-mono), ui-monospace, monospace' }}
    >
      <button
        type="button"
        aria-label="Zoom in"
        onClick={() => zoomBy(0.65)}
        className="w-9 h-9 grid place-items-center rounded-md border-2 select-none"
        style={{
          background: '#FAF8F4', color: '#2D2A24', borderColor: '#3F3B33',
          fontSize: 18, fontWeight: 700, lineHeight: 1,
          boxShadow: '2px 2px 0 #1A1814',
        }}
      >+</button>
      <button
        type="button"
        aria-label="Zoom out"
        onClick={() => zoomBy(1 / 0.65)}
        className="w-9 h-9 grid place-items-center rounded-md border-2 select-none"
        style={{
          background: '#FAF8F4', color: '#2D2A24', borderColor: '#3F3B33',
          fontSize: 22, fontWeight: 700, lineHeight: 1,
          boxShadow: '2px 2px 0 #1A1814',
        }}
      >−</button>
      <button
        type="button"
        aria-label="Reset view"
        onClick={resetView}
        disabled={!zoomedIn}
        className="w-9 h-9 grid place-items-center rounded-md border-2 select-none"
        style={{
          background: zoomedIn ? '#FAF8F4' : '#E8E2D5',
          color: zoomedIn ? '#2D2A24' : '#857D70',
          borderColor: '#3F3B33',
          fontSize: 11, fontWeight: 600, letterSpacing: '0.08em', lineHeight: 1,
          boxShadow: zoomedIn ? '2px 2px 0 #1A1814' : 'none',
          cursor: zoomedIn ? 'pointer' : 'default',
        }}
        title="Reset view (or double-click the map)"
      >RESET</button>
    </div>
  </>
  );
}
