#!/usr/bin/env npx tsx
/**
 * Import real NHD HR river geometry from USGS National Map S3 into rivers.geom.
 *
 * The original `scripts/import-nhd-rivers.ts` queries USGS NHDPlus_HR ArcGIS
 * REST and silently falls back to hand-typed seed coordinates when the WFS
 * call fails. That's why `rivers.geom` was historically a near-straight
 * polyline of ~30 vertices for every floatable river. This script replaces
 * that pipeline with HUC8 shapefile downloads from USGS TNM S3, which is
 * publicly reachable and ships authoritative NHDFlowline data.
 *
 * Pipeline (per river):
 *  1. Download `NHD_H_<HUC8>_HU8_Shape.zip` from prd-tnm.s3.amazonaws.com
 *     (cached in `tmp/nhd/`).
 *  2. Parse `NHDFlowline.shp` with shpjs.
 *  3. Filter to features with the river's `gnis_name` and FCode in
 *     {46006 perennial, 55800 connector, 46000 stream/river-unspecified}.
 *     (46000 is required for rivers like War Eagle Creek whose middle reach is
 *     tagged 46000 — excluding it splits the main stem and drops the lower half.)
 *  4. Build connected components by endpoint adjacency, walk each into a
 *     single LineString, return the LONGEST component (= the main channel).
 *  5. Simplify with Douglas-Peucker tolerance 0.0005 deg (~50m at MO lat).
 *  6. UPDATE rivers SET geom = ST_GeomFromText(...).
 *
 * Run:  npx tsx scripts/import-nhd-rivers-from-tnm.ts                 # all rivers
 *       npx tsx scripts/import-nhd-rivers-from-tnm.ts bourbeuse black # only these slugs
 *       npx tsx scripts/import-nhd-rivers-from-tnm.ts --out tmp/update.sql
 *       npx tsx scripts/import-nhd-rivers-from-tnm.ts bourbeuse --apply  # write to DB
 *
 * The script emits SQL to stdout (or to a file with --out), or writes rows
 * directly with --apply (needs NEXT_PUBLIC_SUPABASE_URL/SUPABASE_URL +
 * SUPABASE_SERVICE_ROLE_KEY/SUPABASE_KEY). Rivers already in prod get their
 * geometry refreshed (mode 'update'); rivers marked mode:'insert' are
 * created if absent, with metadata pulled from scripts/config/nhd-rivers.json
 * and active=false — activation stays a human call after
 * validate_river_data() (see ingestion/DEPLOYMENT-STATUS.md).
 * Emitted SQL can be applied via Supabase SQL editor or:
 *   psql "$DATABASE_URL" -f tmp/update.sql
 *
 * The committed migration `00116_import_real_nhd_river_geometry.sql`
 * already contains the geometry generated by this script for the original
 * seven rivers, so a fresh DB recreated via `npm run db:migrate` will get
 * the same data without needing to re-download HUC8 zips.
 *
 * Requires `tsx` + `shpjs` + `@turf/turf` (already in devDependencies).
 */

